# ============================================================
# [이론 5] groupby & 집계
# ============================================================
#
# df.groupby('컬럼명').집계함수()
#   .mean()  → 그룹별 평균
#   .sum()   → 그룹별 합계
#   .count() → 그룹별 개수
#   .max()   → 그룹별 최댓값
#   .agg({'컬럼': '함수', ...})  → 컬럼별 다른 집계 함수 적용
#
# value_counts(): 값별 빈도 계산
#   df['컬럼'].value_counts()

df = pd.DataFrame({
    '부서': ['개발', '영업', '개발', '영업', '개발'],
    '이름': ['철수', '영희', '민준', '수진', '지훈'],
    '연봉': [5000, 4000, 5500, 3800, 4800]
})

print(df.groupby('부서')['연봉'].mean())    # 부서별 평균 연봉
print(df.groupby('부서')['연봉'].agg(['mean', 'max', 'count'])) # 부서별 연봉의 평균, 최대값, 개수
print(df['부서'].value_counts())           # 부서별 인원 수


