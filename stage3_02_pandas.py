# ============================================================
# [이론 4] 결측치 처리
# ============================================================
#
# 결측치 확인:
#   df.isnull()          → 결측치 여부 (True/False)
#   df.isnull().sum()    → 컬럼별 결측치 수
#
# 결측치 제거:
#   df.dropna()          → 결측치 있는 행 제거
#   df.dropna(axis=1)    → 결측치 있는 열 제거
#
# 결측치 채우기:
#   df.fillna(값)        → 특정 값으로 채우기
#   df.fillna(df.mean()) → 평균으로 채우기

data = {
    '이름': ['철수', '영희', None, '민준'],
    '나이': [25, None, 27, 23],
    '점수': [88, 95, 72, None]
}
df = pd.DataFrame(data)
print(df)
print(df.isnull().sum())     # 컬럼별 결측치 수

df_drop = df.dropna()        # 결측치 있는 행 제거
print(df_drop)

df_fill = df.fillna({'이름': '미정', '나이': df['나이'].mean(), '점수': 0})
print(df_fill)


