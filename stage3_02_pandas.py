# ============================================================
# [이론 2] 데이터 선택 (인덱싱)
# ============================================================
#
# 컬럼 선택:
#   df['컬럼명']          → Series 반환
#   df[['컬럼1', '컬럼2']] → DataFrame 반환
#
# 행 선택:
#   df.loc[인덱스]        → 라벨 기반 선택
#   df.iloc[정수]         → 정수 위치 기반 선택
#
# 조건 필터링:
#   df[df['나이'] > 24]
#   df[(df['나이'] > 24) & (df['점수'] >= 80)]  → 다중 조건 (&, |)

df = pd.DataFrame({
    '이름': ['철수', '영희', '민준', '수진'],
    '나이': [25, 23, 27, 22],
    '점수': [88, 95, 72, 84]
})

print(df['이름'])              # 이름 컬럼
print(df[['이름', '점수']])   # 이름, 점수 컬럼
print(df.loc[1])               # 1번 인덱스 행 
print(df.iloc[0])              # 0번 위치 행

print(df[df['나이'] > 23]) 
print(df[(df['나이'] > 22) & (df['점수'] >= 85)])


