# [실습 5] groupby & 집계
# ============================================================
# 아래 DataFrame을 사용하세요.
sales = pd.DataFrame({
    '지역': ['서울', '부산', '서울', '대구', '부산', '서울'],
    '제품': ['A', 'B', 'A', 'C', 'A', 'B'],
    '판매량': [120, 85, 200, 60, 95, 150],
    '매출': [240000, 170000, 400000, 90000, 190000, 300000]
})
# 1) 지역별 총 매출을 출력하세요.
print(sales.groupby('지역')['매출'].sum())
# 2) 지역별 평균 판매량을 출력하세요.
print(sales.groupby('지역')['판매량'].mean())
# 3) 제품별 판매량 합계와 평균 매출을 함께 출력하세요. (agg 활용)
print(sales.groupby('제품').agg({'판매량': 'sum', '매출': 'mean'}))
# 4) 지역 빈도(몇 번 등장하는지)를 출력하세요.
print(sales['지역'].value_counts())
