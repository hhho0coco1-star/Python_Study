# [이론 3] 데이터 추가 / 수정 / 삭제
# ============================================================
#
# 컬럼 추가/수정:
#   df['새컬럼'] = 값 또는 시리즈
#
# 행 추가:
#   pd.concat([df, new_row], ignore_index=True)
#
# 컬럼 삭제:
#   df.drop('컬럼명', axis=1, inplace=True)  → inplace=True : 원본 수정
#
# 행 삭제:
#   df.drop(인덱스, axis=0)
#
# 값 수정:
#   df.loc[인덱스, '컬럼명'] = 새값

df = pd.DataFrame({
    '이름': ['철수', '영희', '민준'],
    '점수': [88, 95, 72]
})

df['등급'] = ['B', 'A', 'C']           # 컬럼 추가
df['점수'] = df['점수'] + 5             # 컬럼 수정 (점수 5점 보정)
print(df)

df.loc[1, '등급'] = 'A+'               # 특정 값 수정 -> [1번 인덱스의 '등급' 컬럼을 'A+'로 수정]
print(df)

df.drop('등급', axis=1, inplace=True)  
# 컬럼 삭제 -> '등급' 컬럼 삭제 axis=1 : 열 방향, inplace=True : 원본 수정
print(df)


