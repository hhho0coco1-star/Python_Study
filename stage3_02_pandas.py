# 아래 DataFrame을 사용하세요.
survey = pd.DataFrame({
    '이름': ['A', 'B', 'C', 'D', 'E'],
    '나이': [20, None, 25, 30, None],
    '만족도': [4, 5, None, 3, 4],
    '의견': ['좋음', None, '보통', '나쁨', None]
})
# 1) 컬럼별 결측치 수를 출력하세요.
print(survey.isnull().sum())
# 2) '나이' 결측치를 평균으로 채우세요.
survey['나이'] = survey['나이'].fillna(survey['나이'].mean())
# 3) '의견' 결측치를 '미응답'으로 채우세요.
survey['의견'] = survey['의견'].fillna('미응답')
# 4) '만족도' 결측치가 있는 행을 제거하세요.
survey = survey.dropna(subset=['만족도']) # subset : 특정 컬럼에서 결측치 있는 행 제거
# 5) 최종 결과를 출력하세요.
print(survey)
