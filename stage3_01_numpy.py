# ============================================================
# [이론 5] 배열 변환 (reshape, flatten, concatenate, stack)
# ============================================================
#
# arr.reshape(shape)      → 형태 변환 (-1은 자동 계산)
# arr.flatten()           → 1차원으로 펼치기
# np.concatenate([a, b])  → 배열 이어 붙이기 (axis 지정 가능)
# np.vstack([a, b])       → 수직(행) 방향으로 쌓기
# np.hstack([a, b])       → 수평(열) 방향으로 쌓기

arr = np.arange(12)               # [ 0  1  2 ... 11]
print(arr.reshape(3, 4))          # 3x4 행렬
print(arr.reshape(2, -1))         # 2행, 열은 자동 (2x6)
print(arr.reshape(3, 4).flatten()) # 다시 1차원

a = np.array([1, 2, 3])
b = np.array([4, 5, 6])
print(np.concatenate([a, b]))     # [1 2 3 4 5 6]

r1 = np.array([[1, 2], [3, 4]])
r2 = np.array([[5, 6]])
print(np.vstack([r1, r2]))        # 수직으로 붙이기
# [[1 2]
#  [3 4]
#  [5 6]]

c1 = np.array([[1], [2]])
c2 = np.array([[3], [4]])
print(np.hstack([c1, c2]))        # 수평으로 붙이기
# [[1 3]
#  [2 4]]


