# ============================================================
# [이론 4] 수학 통계 함수
# ============================================================
#
# np.sum(arr)         → 전체 합
# np.mean(arr)        → 평균
# np.std(arr)         → 표준편차
# np.min(arr)         → 최솟값
# np.max(arr)         → 최댓값
# np.argmin(arr)      → 최솟값의 인덱스
# np.argmax(arr)      → 최댓값의 인덱스
# np.sort(arr)        → 정렬된 배열 반환 (원본 유지)
# np.cumsum(arr)      → 누적 합
#
# axis 인자: 2차원 배열에서 축 방향 지정
#   axis=0 → 열 방향 (행끼리 연산, 결과는 각 열)
#   axis=1 → 행 방향 (열끼리 연산, 결과는 각 행)

data = np.array([4, 7, 2, 9, 1, 5, 8, 3, 6])
print(np.sum(data))        # 45
print(np.mean(data))       # 5.0
print(np.std(data))        # 2.581...
print(np.min(data))        # 1
print(np.max(data))        # 9
print(np.argmax(data))     # 3  (9의 인덱스)
print(np.sort(data))       # [1 2 3 4 5 6 7 8 9]
print(np.cumsum(data))     # [ 4 11 13 22 23 28 36 39 45]

mat = np.array([[1, 5, 3],
                [4, 2, 6]])
print(np.sum(mat, axis=0))   # [5 7 9]   열 방향 합
print(np.sum(mat, axis=1))   # [9 12]    행 방향 합
print(np.mean(mat, axis=1))  # [3. 4.]   행 방향 평균


