import numpy as np

# ============================================================
# [이론 1] 배열 생성
# ============================================================
#
# np.array(list)           → 리스트로 배열 생성
# np.zeros(shape)          → 0으로 채운 배열
# np.ones(shape)           → 1로 채운 배열
# np.arange(start,stop,step) → range()와 유사, ndarray 반환
# np.linspace(start,stop,n)  → start~stop 사이 n개 균등 분할
# np.full(shape, value)    → 특정 값으로 채운 배열
#
# 배열 정보:
#   arr.shape   → 형태 (행, 열)
#   arr.dtype   → 원소 타입 (int64, float64 등)
#   arr.ndim    → 차원 수
#   arr.size    → 전체 원소 수

a = np.array([1, 2, 3, 4, 5])
print(a)           # [1 2 3 4 5]
print(a.shape)     # (5,)
print(a.dtype)     # int64

b = np.array([[1, 2, 3], [4, 5, 6]])   # 2차원 배열
print(b)        # [[1 2 3][4 5 6]]
print(b.shape)     # (2, 3) → 2행 3열
print(b.ndim)      # 2
print(b.size)      # 6

print(np.zeros((2, 3)))                 # 2x3 영행렬
print(np.ones((3, 3)))                  # 3x3 단위 행렬
print(np.arange(0, 10, 2))             # [0 2 4 6 8]
print(np.linspace(0, 1, 5))            # [0.   0.25 0.5  0.75 1.  ]
print(np.full((2, 2), 7))              # [[7 7][7 7]]


