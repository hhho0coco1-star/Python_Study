# ============================================================
# [이론 3] 배열 연산 & 브로드캐스팅
# ============================================================
#
# 배열 연산: 같은 형태의 배열끼리 원소별(element-wise) 연산
#   a + b, a - b, a * b, a / b  → 원소별 적용
#   a ** 2                       → 제곱
#
# 브로드캐스팅: 형태가 다른 배열 간 자동 확장 연산
#   (3,3) + (3,)   → 각 행에 1차원 배열을 더함
#   (3,1) + (1,3)  → 자동으로 (3,3)으로 확장
#
# 스칼라 연산: 배열 + 숫자 → 모든 원소에 적용

x = np.array([1, 2, 3, 4])
y = np.array([10, 20, 30, 40])

print(x + y)       # [11 22 33 44]
print(x * y)       # [10 40 90 160]
print(x ** 2)      # [ 1  4  9 16]
print(x + 100)     # [101 102 103 104]  스칼라 브로드캐스팅

mat = np.array([[1, 2, 3],
                [4, 5, 6]])
row = np.array([10, 20, 30])
print(mat + row)   # 각 행에 [10,20,30]을 더함
# [[11 22 33]
#  [14 25 36]]


